let notas = 90

if (notas >= 90){
  console.log(notas = "A")
} else if (notas >= 80 && notas <= 89) {
  console.log(notas = "B")
} else if (notas >= 70 && notas <= 79){
  console.log(notas = "C")
} else if (notas >= 60 && notas <= 69){
  console.log(notas = "D")
} else {
  console.log(notas = "F")
}
